#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr_s;
	size_t			i;

	ptr_s = s;
	i = 0;
	if (n == 0)
	{
		return ;
	}
	while (i < n)
	{
		ptr_s[i] = '\0';
		i++;
	}
}
